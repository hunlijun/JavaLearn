/**
 * 创建型模式
 * <p>
 * 在软件工程中，创建型模式是处理对象创建的设计模式，试图根据实际情况使用
 * 合适的方式创建对象。基本的对象创建方式可能会导致设计上的问题，或增加设
 * 计的复杂度。创建型模式通过以某种方式控制对象的创建来解决问题。
 * <p>
 * 创建型模式由两个主导思想构成。一是将系统使用的具体类封装起来，二是隐藏
 * 这些具体类的实例创建和结合的方式。
 * <p>
 * 创建型模式又分为对象创建型模式和类创建型模式。对象创建型模式处理对象的
 * 创建，类创建型模式处理类的创建。详细地说，对象创建型模式把对象创建的一
 * 部分推迟到另一个对象中，而类创建型模式将它对象的创建推迟到子类中。
 */
